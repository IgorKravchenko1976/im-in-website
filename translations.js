// Translations manager
let translations = translationsData;
const _urlLang = new URLSearchParams(window.location.search).get('lang');
let currentLanguage = _urlLang || localStorage.getItem('language') || 'uk';

function getTranslation(key, lang = currentLanguage) {
    if (!translations[lang]) {
        lang = 'uk';
    }
    const keys = key.split('.');
    let value = translations[lang];
    for (const k of keys) {
        if (value && typeof value === 'object') {
            value = value[k];
        } else {
            return key;
        }
    }
    return value || key;
}

function setLanguage(lang) {
    if (!translations[lang]) {
        lang = 'uk';
    }
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    document.documentElement.lang = lang;

    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = getTranslation(key, lang);
        if (typeof translation === 'string' && translation.includes('<span')) {
            element.innerHTML = translation;
        } else {
            element.textContent = translation;
        }
    });

    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        const translation = getTranslation(key, lang);
        if (typeof translation === 'string') {
            element.placeholder = translation;
        }
    });

    updateLanguageSelector();
}

function updateLanguageSelector() {
    const currentLangEl = document.querySelector('.current-language');
    if (currentLangEl) {
        currentLangEl.textContent = currentLanguage.toUpperCase();
    }
    document.querySelectorAll('.language-option').forEach(option => {
        const lang = option.getAttribute('data-lang');
        option.classList.toggle('active', lang === currentLanguage);
    });
}

function initTranslations() {
    setLanguage(currentLanguage);

    const languageBtn = document.querySelector('.language-btn');
    const languageSelector = document.querySelector('.language-selector');

    if (languageBtn && languageSelector) {
        languageBtn.addEventListener('click', (e) => {
            e.stopPropagation();
            languageSelector.classList.toggle('active');
        });
        document.addEventListener('click', (e) => {
            if (!languageSelector.contains(e.target)) {
                languageSelector.classList.remove('active');
            }
        });
        document.querySelectorAll('.language-option').forEach(option => {
            option.addEventListener('click', (e) => {
                e.stopPropagation();
                const lang = option.getAttribute('data-lang');
                if (lang && translations[lang]) {
                    setLanguage(lang);
                    languageSelector.classList.remove('active');
                }
            });
        });
    }
}

if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initTranslations);
} else {
    initTranslations();
}

window.translationsManager = {
    setLanguage,
    getTranslation,
    currentLanguage: () => currentLanguage
};
